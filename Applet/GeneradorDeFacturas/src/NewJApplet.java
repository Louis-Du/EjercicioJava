    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String nombreCliente = txtNombreCliente.getText();
        
        int valorUnitarioProducto1 = Integer.parseInt(txtValorUnitarioProducto1.getText());
        int valorUnitarioProducto2 = Integer.parseInt(txtValorUnitarioProducto2.getText());
        int valorUnitarioProducto3 = Integer.parseInt(txtValorUnitarioProducto3.getText());

        int cantidadTotalProducto1 = Integer.parseInt(txtCantidadProducto1.getText());
        int cantidadTotalProducto2 = Integer.parseInt(txtCantidadProducto2.getText());
        int cantidadTotalProducto3 = Integer.parseInt(txtCantidadProducto3.getText());

        int valorTotalProducto1 = valorUnitarioProducto1 * cantidadTotalProducto1;
        int valorTotalProducto2 = valorUnitarioProducto2 * cantidadTotalProducto2;
        int valorTotalProducto3 = valorUnitarioProducto3 * cantidadTotalProducto3;

        txtValorTotalProducto1.setText(String.valueOf(valorTotalProducto1));
        txtValorTotalProducto2.setText(String.valueOf(valorTotalProducto2));
        txtValorTotalProducto3.setText(String.valueOf(valorTotalProducto3));

        int valorTotal = valorTotalProducto1 + valorTotalProducto2 + valorTotalProducto3;
        txtValorTotalProductos.setText(String.valueOf(valorTotal));

        String resultadoFactura = "Se√±or/a " + nombreCliente + ", el total de su compra es de " + valorTotal;
        jLabelResultado.setText(resultadoFactura);
    }//GEN-LAST:event_btnCalcularActionPerformed


